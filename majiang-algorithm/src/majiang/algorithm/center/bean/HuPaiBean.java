package majiang.algorithm.center.bean;

public class HuPaiBean {
	
	/**
	 * 胡牌类型
	 */
	private Integer huPaiType;
	
	/**
	 * 牌
	 */
	private String pai;
	
	/**
	 * 张数
	 */
	private Integer size;

	public Integer getHuPaiType() {
		return huPaiType;
	}

	public void setHuPaiType(Integer huPaiType) {
		this.huPaiType = huPaiType;
	}

	public String getPai() {
		return pai;
	}

	public void setPai(String pai) {
		this.pai = pai;
	}

	public Integer getSize() {
		return size;
	}

	public void setSize(Integer size) {
		this.size = size;
	}

}
